// Crea una función llamada sumar que tome dos números como parámetros y devuelva su suma. Prueba la función con diferentes números.

function suma(a, b){
    return `${a} + ${b} = ${a + b}`
}

console.log(suma(1,2))
console.log(suma(4,3))
console.log(suma(9,11))
